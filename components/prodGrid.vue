<!-- 该组件用于以宫格模式展现商品 -->
<template>
	<view class="wrap">
		<u-waterfall v-model="flowList" ref="uWaterfall">
			<template v-slot:left="{leftList}">
				<view class="demo-warter" v-for="(item, index) in leftList" :key="index" @click="gotoProduct(item)">
					<u-lazy-load threshold="-450" border-radius="10" :image="item.goodsUrl" :index="index">
					</u-lazy-load>
					<view class="demo-title u-line-2">
						{{item.title}}
					</view>
					<view class="demo-price">
						{{item.price}}元
					</view>
					<view class="demo-tag">
						<view class="demo-tag-owner">
							自营
						</view>
						<view class="demo-tag-text">
							放心购
						</view>
					</view>
					<u-icon name="close-circle-fill" color="#fa3534" size="34" class="u-close" @click="remove(item.id)">
					</u-icon>
				</view>
			</template>
			<template v-slot:right="{rightList}">
				<view class="demo-warter" v-for="(item, index) in rightList" :key="index" @click="gotoProduct(item)">
					<u-lazy-load threshold="-450" border-radius="10" :image="item.goodsUrl" :index="index">
					</u-lazy-load>
					<view class="demo-title u-line-2">
						{{item.title}}
					</view>
					<view class="demo-price">
						{{item.price}}元
					</view>
					<view class="demo-tag">
						<view class="demo-tag-owner">
							自营
						</view>
						<view class="demo-tag-text">
							放心购
						</view>
					</view>
					<u-icon name="close-circle-fill" color="#fa3534" size="34" class="u-close" @click="remove(item.id)">
					</u-icon>
				</view>
			</template>
		</u-waterfall>
		<u-loadmore bg-color="rgb(240, 240, 240)" :status="loadStatus" @loadmore="addRandomData"></u-loadmore>
	</view>
</template>

<script>
	export default {
		name: "itemShowList",
		props: ['list'],
		data() {
			return {
				loadStatus: 'loadmore',
				flowList: [],
			}
		},
		onLoad() {
			this.addRandomData();
		},
		onReachBottom() {
			this.loadStatus = 'loading';
			// 模拟数据加载
			setTimeout(() => {
				this.addRandomData();
				this.loadStatus = 'loadmore';
			}, 1000)
		},
		methods: {
			// 进入商品详情页
			gotoProduct(obj) {
				uni.navigateTo({
					url: `/pages/product/productDetail?id=${obj.id}`,
					// success: () => {
					// setTimeout(() => {
					// 	obj.timestamp = 888888
					// 	obj.isCollect = false
					// 	obj.isKill = true
					// 	uni.$emit('gotoProdDetail', JSON.parse(JSON.stringify(obj)))
					// }, 500) 
					// }
				})
			},
			addRandomData() {
				for (let i = 0; i < 10; i++) {
					let index = this.$u.random(0, this.list.length - 1);
					// 先转成字符串再转成对象，避免数组对象引用导致数据混乱
					let item = JSON.parse(JSON.stringify(this.list[index]))
					item.id = this.$u.guid();
					this.flowList.push(item);
				}
			},
			remove(id) {
				this.$refs.uWaterfall.remove(id);
			},
			clear() {
				this.$refs.uWaterfall.clear();
			}
		}
	}
</script>


